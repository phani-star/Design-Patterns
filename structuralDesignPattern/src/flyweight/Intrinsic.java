package flyweight;

public class Intrinsic {

	String batter;
	String chutney;
	
	public Intrinsic(String batter, String chutney) {
		this.batter = batter;
		this.chutney = chutney;
	}

	public String getBatter() {
		return batter;
	}

	public void setBatter(String batter) {
		this.batter = batter;
	}

	public String getChutney() {
		return chutney;
	}

	public void setChutney(String chutney) {
		this.chutney = chutney;
	}
	
	
	
	
}
